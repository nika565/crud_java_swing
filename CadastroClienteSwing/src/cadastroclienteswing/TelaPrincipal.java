    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linhaSelecionada = TabelaClientes.getSelectedRow();
        
        if(linhaSelecionada >= 0){
            int result = JOptionPane.showConfirmDialog(this, "Deseja realmente alterar esse cliente?", "CUIDADO!", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            if(result == JOptionPane.YES_OPTION){
                
                String nome = txtNome.getText();
                String cpf = txtCPF.getText();
                String telefone = txtTelefone.getText();
                String endereco = txtEndereco.getText();
                String numero = txtNumero.getText();
                String cidade = txtCidade.getText();
                String estado = txtEstado.getText();
                
                if(!isCamposValidos(nome, cpf)){
                    JOptionPane.showMessageDialog(null, "Existem campos a serem preenchidos", "ATENÇÃO", JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                
                Cliente cliente = new Cliente(nome, cpf, telefone, endereco, numero, cidade, estado);
                
                this.clienteDAO.alterar(cliente);
                
                // Valores para ser atualizados
                Long cpfLong = cliente.getCpf();
                Cliente buscarCliente = this.clienteDAO.consultar(cpfLong);
                
                updateRow(TabelaClientes, linhaSelecionada, buscarCliente);
                
                limparCampos();
                JOptionPane.showMessageDialog(null, "Cliente alterado com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um campo.", "ERRO", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private static void updateRow(JTable table, int rowIndex, Cliente cliente) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();

        // Supondo que a tabela tem 3 colunas correspondentes aos atributos do Cliente
        model.setValueAt(cliente.getNome(), rowIndex, 0);
        model.setValueAt(cliente.getCpf(), rowIndex, 1);
        model.setValueAt(cliente.getTel(), rowIndex, 2);
        model.setValueAt(cliente.getEnd(), rowIndex, 3);
        model.setValueAt(cliente.getNumero(), rowIndex, 4);
        model.setValueAt(cliente.getCidade(), rowIndex, 5);
        model.setValueAt(cliente.getEstado(), rowIndex, 6);
    }
    

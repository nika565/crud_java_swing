    private boolean isCamposValidos(String ...campos) {
        
        // Iterando sobre os campos
        for(String campo : campos){
            
            // Verificando campos vazios
            if(campo == null || "".equals(campo)){
                return false;
            }
            
                        
        }
        
        return true;
    }

